`timescale 1ns/1ps

module tb_decode;

// ---------------------------------------------------------------
// Inputs
// ---------------------------------------------------------------
reg         clk;
reg         rst;
reg         valid;
reg         reg_write_en_in;
reg         load_control_signal;
reg  [31:0] instruction;
reg  [31:0] pc_address;
reg  [31:0] rd_wb_data;
reg  [31:0] instruction_rd;

// ---------------------------------------------------------------
// Outputs
// ---------------------------------------------------------------
wire        load;
wire        store;
wire        jalr;
wire        next_sel;
wire        branch_result;
wire        reg_write_en_out;
wire [3:0]  alu_control;
wire [1:0]  mem_to_reg;
wire [4:0]  rs1, rs2;
wire [31:0] opb_data;
wire [31:0] opa_mux_out;
wire [31:0] opb_mux_out;

// ---------------------------------------------------------------
// DUT Instantiation
// ---------------------------------------------------------------
decode DUT (
    .clk                (clk),
    .rst                (rst),
    .valid              (valid),
    .reg_write_en_in    (reg_write_en_in),
    .load_control_signal(load_control_signal),
    .instruction        (instruction),
    .pc_address         (pc_address),
    .rd_wb_data         (rd_wb_data),
    .instruction_rd     (instruction_rd),
    .load               (load),
    .store              (store),
    .jalr               (jalr),
    .next_sel           (next_sel),
    .branch_result      (branch_result),
    .reg_write_en_out   (reg_write_en_out),
    .alu_control        (alu_control),
    .mem_to_reg         (mem_to_reg),
    .rs1                (rs1),
    .rs2                (rs2),
    .opb_data           (opb_data),
    .opa_mux_out        (opa_mux_out),
    .opb_mux_out        (opb_mux_out)
);

// ---------------------------------------------------------------
// Initial Block
// ---------------------------------------------------------------
initial begin

    $fsdbDumpvars();
    $monitor("Time=%0t instr=%h pc=%h | load=%b store=%b jalr=%b next_sel=%b | reg_wr=%b alu_ctrl=%b | rs1=%0d rs2=%0d | opa=%h opb=%h | branch=%b",
              $time, instruction, pc_address,
              load, store, jalr, next_sel,
              reg_write_en_out, alu_control,
              rs1, rs2,
              opa_mux_out, opb_mux_out,
              branch_result);

    // -----------------------------------------------------------
    // Initialise all inputs
    // -----------------------------------------------------------
    clk                 = 1'b0;
    rst                 = 1'b1;
    valid               = 1'b0;
    reg_write_en_in     = 1'b0;
    load_control_signal = 1'b0;
    instruction         = 32'h0000_0013; // NOP (ADDI x0,x0,0)
    pc_address          = 32'h0000_0000;
    rd_wb_data          = 32'h0000_0000;
    instruction_rd      = 32'h0000_0000;
    #10;

    // -----------------------------------------------------------
    // Release reset
    // -----------------------------------------------------------
    rst   = 1'b0;
    valid = 1'b1;
    #10;

    // -----------------------------------------------------------
    // Test ADD x1, x2, x3
    // R-type | funct7=0000000 | rs2=x3 | rs1=x2 | f3=000 | rd=x1 | op=0110011
    // -----------------------------------------------------------
    instruction = {7'b0000000, 5'd3, 5'd2, 3'b000, 5'd1, 7'b0110011};
    pc_address  = 32'h0000_0100; #10;

    // -----------------------------------------------------------
    // Test SUB x4, x5, x6
    // R-type | funct7=0100000 | rs2=x6 | rs1=x5 | f3=000 | rd=x4 | op=0110011
    // -----------------------------------------------------------
    instruction = {7'b0100000, 5'd6, 5'd5, 3'b000, 5'd4, 7'b0110011};
    pc_address  = 32'h0000_0104; #10;

    // -----------------------------------------------------------
    // Test AND x7, x8, x9
    // R-type | funct7=0000000 | rs2=x9 | rs1=x8 | f3=111 | rd=x7 | op=0110011
    // -----------------------------------------------------------
    instruction = {7'b0000000, 5'd9, 5'd8, 3'b111, 5'd7, 7'b0110011};
    pc_address  = 32'h0000_0108; #10;

    // -----------------------------------------------------------
    // Test OR x10, x11, x12
    // R-type | funct7=0000000 | rs2=x12 | rs1=x11 | f3=110 | rd=x10 | op=0110011
    // -----------------------------------------------------------
    instruction = {7'b0000000, 5'd12, 5'd11, 3'b110, 5'd10, 7'b0110011};
    pc_address  = 32'h0000_010C; #10;

    // -----------------------------------------------------------
    // Test XOR x13, x14, x15
    // R-type | funct7=0000000 | rs2=x15 | rs1=x14 | f3=100 | rd=x13 | op=0110011
    // -----------------------------------------------------------
    instruction = {7'b0000000, 5'd15, 5'd14, 3'b100, 5'd13, 7'b0110011};
    pc_address  = 32'h0000_0110; #10;

    // -----------------------------------------------------------
    // Test ADDI x1, x0, 42
    // I-type | imm=42 | rs1=x0 | f3=000 | rd=x1 | op=0010011
    // -----------------------------------------------------------
    instruction = {12'd42, 5'd0, 3'b000, 5'd1, 7'b0010011};
    pc_address  = 32'h0000_0200; #10;

    // -----------------------------------------------------------
    // Test ORI x2, x0, 0xFF
    // I-type | imm=0xFF | rs1=x0 | f3=110 | rd=x2 | op=0010011
    // -----------------------------------------------------------
    instruction = {12'hFF, 5'd0, 3'b110, 5'd2, 7'b0010011};
    pc_address  = 32'h0000_0204; #10;

    // -----------------------------------------------------------
    // Test ANDI x3, x0, 0x0F
    // I-type | imm=0x0F | rs1=x0 | f3=111 | rd=x3 | op=0010011
    // -----------------------------------------------------------
    instruction = {12'h0F, 5'd0, 3'b111, 5'd3, 7'b0010011};
    pc_address  = 32'h0000_0208; #10;

    // -----------------------------------------------------------
    // Test ADDI x4, x0, -1   (negative immediate, imm=0xFFF)
    // I-type | imm=0xFFF | rs1=x0 | f3=000 | rd=x4 | op=0010011
    // -----------------------------------------------------------
    instruction = {12'hFFF, 5'd0, 3'b000, 5'd4, 7'b0010011};
    pc_address  = 32'h0000_020C; #10;

    // -----------------------------------------------------------
    // Test LW x5, 16(x1)
    // I-type | imm=16 | rs1=x1 | f3=010 | rd=x5 | op=0000011
    // -----------------------------------------------------------
    instruction = {12'd16, 5'd1, 3'b010, 5'd5, 7'b0000011};
    pc_address  = 32'h0000_0300; #10;

    // -----------------------------------------------------------
    // Test LB x6, 4(x2)
    // I-type | imm=4 | rs1=x2 | f3=000 | rd=x6 | op=0000011
    // -----------------------------------------------------------
    instruction = {12'd4, 5'd2, 3'b000, 5'd6, 7'b0000011};
    pc_address  = 32'h0000_0304; #10;

    // -----------------------------------------------------------
    // Test SW x5, 8(x2)
    // S-type | imm[11:5]=0000000 | rs2=x5 | rs1=x2 | f3=010 | imm[4:0]=01000 | op=0100011
    // -----------------------------------------------------------
    instruction = {7'b0000000, 5'd5, 5'd2, 3'b010, 5'b01000, 7'b0100011};
    pc_address  = 32'h0000_0400; #10;

    // -----------------------------------------------------------
    // Test SB x7, 3(x3)
    // S-type | imm[11:5]=0000000 | rs2=x7 | rs1=x3 | f3=000 | imm[4:0]=00011 | op=0100011
    // -----------------------------------------------------------
    instruction = {7'b0000000, 5'd7, 5'd3, 3'b000, 5'b00011, 7'b0100011};
    pc_address  = 32'h0000_0404; #10;

    // -----------------------------------------------------------
    // Writeback x1 = 0xDEADBEEF  (pre-load for branch test)
    // -----------------------------------------------------------
    instruction_rd  = {20'b0, 5'd1, 7'b0};
    rd_wb_data      = 32'hDEAD_BEEF;
    reg_write_en_in = 1'b1; #10;
    reg_write_en_in = 1'b0;

    // Writeback x2 = 0xDEADBEEF
    instruction_rd  = {20'b0, 5'd2, 7'b0};
    rd_wb_data      = 32'hDEAD_BEEF;
    reg_write_en_in = 1'b1; #10;
    reg_write_en_in = 1'b0; #10;

    // -----------------------------------------------------------
    // Test BEQ x1, x2, +20   (taken: x1==x2)
    // B-type | imm[12]=0 | imm[10:5]=000000 | rs2=x2 | rs1=x1 |
    //         f3=000 | imm[4:1]=1010 | imm[11]=0 | op=1100011
    // -----------------------------------------------------------
    instruction = {1'b0, 6'b000000, 5'd2, 5'd1, 3'b000, 4'b1010, 1'b0, 7'b1100011};
    pc_address  = 32'h0000_0500; #10;

    // -----------------------------------------------------------
    // Test BNE x1, x2, +8   (not taken: x1==x2)
    // B-type | imm[12]=0 | imm[10:5]=000000 | rs2=x2 | rs1=x1 |
    //         f3=001 | imm[4:1]=0100 | imm[11]=0 | op=1100011
    // -----------------------------------------------------------
    instruction = {1'b0, 6'b000000, 5'd2, 5'd1, 3'b001, 4'b0100, 1'b0, 7'b1100011};
    pc_address  = 32'h0000_0504; #10;

    // -----------------------------------------------------------
    // Test BLT x1, x2, +8   (not taken: x1==x2)
    // B-type | f3=100
    // -----------------------------------------------------------
    instruction = {1'b0, 6'b000000, 5'd2, 5'd1, 3'b100, 4'b0100, 1'b0, 7'b1100011};
    pc_address  = 32'h0000_0508; #10;

    // -----------------------------------------------------------
    // Test JAL x1, +100
    // J-type | imm[20]=0 | imm[10:1]=0110010000 | imm[11]=0 |
    //          imm[19:12]=00000000 | rd=x1 | op=1101111
    // -----------------------------------------------------------
    instruction = {1'b0, 10'b0110010000, 1'b0, 8'b00000000, 5'd1, 7'b1101111};
    pc_address  = 32'h0000_0600; #10;

    // -----------------------------------------------------------
    // Test JALR x0, 0(x1)
    // I-type | imm=0 | rs1=x1 | f3=000 | rd=x0 | op=1100111
    // -----------------------------------------------------------
    instruction = {12'd0, 5'd1, 3'b000, 5'd0, 7'b1100111};
    pc_address  = 32'h0000_0700; #10;

    // -----------------------------------------------------------
    // Test LUI x1, 0xABCDE
    // U-type | imm[31:12]=0xABCDE | rd=x1 | op=0110111
    // -----------------------------------------------------------
    instruction = {20'hABCDE, 5'd1, 7'b0110111};
    pc_address  = 32'h0000_0800; #10;

    // -----------------------------------------------------------
    // Test AUIPC x2, 0x10
    // U-type | imm[31:12]=0x10 | rd=x2 | op=0010111
    // -----------------------------------------------------------
    instruction = {20'h00010, 5'd2, 7'b0010111};
    pc_address  = 32'h0000_0900; #10;

    // -----------------------------------------------------------
    // Test valid=0   (pipeline stall - all outputs suppressed)
    // -----------------------------------------------------------
    valid       = 1'b0;
    instruction = {7'b0000000, 5'd3, 5'd2, 3'b000, 5'd1, 7'b0110011};
    pc_address  = 32'h0000_0A00; #10;
    valid       = 1'b1;

    // -----------------------------------------------------------
    // Test load_control_signal=1   (load-use hazard stall)
    // -----------------------------------------------------------
    load_control_signal = 1'b1;
    instruction = {12'd42, 5'd0, 3'b000, 5'd1, 7'b0010011};
    pc_address  = 32'h0000_0B00; #10;
    load_control_signal = 1'b0;

    // -----------------------------------------------------------
    // Test Reset
    // -----------------------------------------------------------
    rst = 1'b1; #10;
    rst = 1'b0; #10;

    $finish;
end

// ---------------------------------------------------------------
// Clock Generator
// ---------------------------------------------------------------
always
begin
    clk = ~clk;
    #5;
end

endmodule